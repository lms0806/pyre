                let changed0 =
                    ctx.with_intbound_mut(&arg0_box, |bm| matches!(bm.intersect(&b), Ok(true)));
                if changed0 {
                    self.propagate_bounds_backward(&arg0.to_boxref(), ctx);
                }
                let b = r.sub_bound(&b1);
                let changed1 =
                    ctx.with_intbound_mut(&arg1_box, |bm| matches!(bm.intersect(&b), Ok(true)));
