//! `OpcodeHandler` trait impls for `MIFrame` — the tracing meta-interpreter
//! frame. This is the analog of `pypy/jit/metainterp/pyjitpl.py`'s
//! `MIFrame.opimpl_*` methods: the concrete interpreter implements the same
//! `OpcodeHandler` traits by hand in `pyre-interpreter/src/eval.rs` (for
//! `PyFrame`); these impls are the trace-time twin.
//!
//! `pyjitpl.py` generates its *regular* opimpl families (`pyjitpl.py:279-368`)
//! with `exec(py.code.Source(template).compile())` over a name list, and
//! hand-writes the irregular ones with `@arguments`. We mirror that
//! abstraction directly: the uniform trivial-constant family is generated by
//! `trivial_constant_methods!` (the `exec(Source(...))` analog, expanded and
//! type-checked in place by rustc); the irregular constants and the other
//! traits are written out as plain methods.
//!
//! **drift warning**: these bodies are a transcription of `MIFrame` helper
//! logic; the helpers they call live in the same crate, so changing a helper
//! signature almost certainly means changing the matching method here.

use pyre_interpreter::bytecode::{BinaryOperator, ComparisonOperator};

impl pyre_interpreter::SharedOpcodeHandler for crate::state::MIFrame {
    type Value = crate::state::FrontendOp;

    fn push_value(&mut self, value: Self::Value) -> Result<(), pyre_interpreter::PyError> {
        self.with_ctx(|this, ctx| {
            crate::state::MIFrame::push_value(this, ctx, value.opref, value.concrete);
            Ok(())
        })
    }

    fn pop_value(&mut self) -> Result<Self::Value, pyre_interpreter::PyError> {
        let s = self.sym();
        let stack_idx = s.valuestackdepth.checked_sub(s.nlocals + 1).unwrap_or(0);
        let concrete = s
            .concrete_stack
            .get(stack_idx)
            .copied()
            .unwrap_or(crate::state::ConcreteValue::Null);
        let opref = self.with_ctx(|this, ctx| crate::state::MIFrame::pop_value(this, ctx))?;
        Ok(crate::state::FrontendOp::new(opref, concrete))
    }

    fn peek_at(&mut self, depth: usize) -> Result<Self::Value, pyre_interpreter::PyError> {
        let s = self.sym();
        let stack_idx = s.valuestackdepth.checked_sub(s.nlocals + depth + 1);
        let concrete = stack_idx
            .and_then(|idx| s.concrete_stack.get(idx).copied())
            .unwrap_or(crate::state::ConcreteValue::Null);
        let opref =
            self.with_ctx(|this, ctx| crate::state::MIFrame::peek_value(this, ctx, depth))?;
        Ok(crate::state::FrontendOp::new(opref, concrete))
    }

    fn guard_nonnull_value(&mut self, value: Self::Value) -> Result<(), pyre_interpreter::PyError> {
        self.with_ctx(|this, ctx| {
            crate::state::MIFrame::guard_nonnull(this, ctx, value.opref);
            Ok(())
        })
    }

    fn make_function(
        &mut self,
        code_obj: Self::Value,
    ) -> Result<Self::Value, pyre_interpreter::PyError> {
        use crate::helpers::TraceHelperAccess;
        let opref = self.trace_make_function(code_obj.opref)?;
        Ok(crate::state::FrontendOp::void(opref))
    }

    fn call_callable(
        &mut self,
        callable: Self::Value,
        args: &[Self::Value],
    ) -> Result<Self::Value, pyre_interpreter::PyError> {
        // executor.execute_varargs parity: compute concrete result.
        let concrete_callable = callable.concrete.to_pyobj();
        let concrete_args: Vec<pyre_object::PyObjectRef> =
            args.iter().map(|a| a.concrete.to_pyobj()).collect();
        let args_available =
            !concrete_callable.is_null() && concrete_args.iter().all(|v| !v.is_null());
        let mut result_concrete = crate::state::ConcreteValue::Null;
        // Builtin callees cannot be inlined; compute their concrete result up
        // front (no trace-through path exists for them).
        let is_user_fn = args_available
            && unsafe {
                pyre_interpreter::is_function(concrete_callable)
                    && !pyre_interpreter::is_builtin_code(pyre_interpreter::getcode(
                        concrete_callable,
                    )
                        as pyre_object::PyObjectRef)
            };
        if args_available && !is_user_fn {
            unsafe {
                if pyre_interpreter::is_function(concrete_callable)
                    && pyre_interpreter::is_builtin_code(pyre_interpreter::getcode(
                        concrete_callable,
                    )
                        as pyre_object::PyObjectRef)
                {
                    let code = pyre_interpreter::getcode(concrete_callable);
                    let func = pyre_interpreter::builtin_code_get(code as pyre_object::PyObjectRef);
                    let result = func(&concrete_args).unwrap_or(pyre_object::PY_NULL);
                    result_concrete = crate::state::ConcreteValue::from_pyobj(result);
                }
            }
        }
        let arg_oprefs: Vec<majit_ir::OpRef> = args.iter().map(|a| a.opref).collect();
        let opref = self.call_callable_value(
            callable.opref,
            &arg_oprefs,
            concrete_callable,
            &concrete_args,
        )?;
        // pyjitpl.py:2025 concrete execution only — for residual calls.
        //
        // RPython decides perform_call (inline) vs do_residual_call BEFORE
        // executing, and only residual calls run the callee as a whole; an
        // inlined callee is traced through and its concrete values come from
        // the inline framestack. Pyre's snapshot trace shares the heap with
        // the interpreter that re-runs this iteration, so executing a callee
        // here commits its heap side effects, and the re-run repeats them
        // (issue #143). Mirror RPython: skip the whole-callee execution when
        // `call_callable_value` chose to inline (pending_inline_frame set) —
        // the inline frame's per-opcode concrete steps defer stores like the
        // root tracer does.
        if is_user_fn && self.pending_inline_frame.is_none() {
            unsafe {
                use std::cell::Cell;
                thread_local! {
                    static CONCRETE_CALL_DEPTH: Cell<u32> = Cell::new(0);
                }
                let depth = CONCRETE_CALL_DEPTH.with(|d| d.get());
                if depth < 32 {
                    CONCRETE_CALL_DEPTH.with(|d| d.set(depth + 1));
                    let exec_ctx = self.sym().concrete_execution_context;
                    let result = pyre_interpreter::call::call_user_function_plain_with_ctx(
                        exec_ctx,
                        concrete_callable,
                        &concrete_args,
                    );
                    CONCRETE_CALL_DEPTH.with(|d| d.set(depth));
                    if let Ok(result) = result {
                        result_concrete = crate::state::ConcreteValue::from_pyobj(result);
                    }
                }
            }
        }
        Ok(crate::state::FrontendOp::new(opref, result_concrete))
    }

    fn build_list(
        &mut self,
        items: &[Self::Value],
    ) -> Result<Self::Value, pyre_interpreter::PyError> {
        use crate::helpers::TraceHelperAccess;
        let concrete_items: Vec<pyre_object::PyObjectRef> =
            items.iter().map(|i| i.concrete.to_pyobj()).collect();
        let mut result_concrete = crate::state::ConcreteValue::Null;
        if concrete_items.iter().all(|v| !v.is_null()) {
            let list = pyre_interpreter::build_list_from_refs(&concrete_items);
            result_concrete = crate::state::ConcreteValue::from_pyobj(list);
        }
        let item_oprefs: Vec<majit_ir::OpRef> = items.iter().map(|i| i.opref).collect();
        let opref = self.trace_build_list(&item_oprefs)?;
        Ok(crate::state::FrontendOp::new(opref, result_concrete))
    }

    fn build_tuple(
        &mut self,
        items: &[Self::Value],
    ) -> Result<Self::Value, pyre_interpreter::PyError> {
        let concrete_items: Vec<pyre_object::PyObjectRef> =
            items.iter().map(|i| i.concrete.to_pyobj()).collect();
        let mut result_concrete = crate::state::ConcreteValue::Null;
        if concrete_items.iter().all(|v| !v.is_null()) {
            let tuple = pyre_interpreter::build_tuple_from_refs(&concrete_items);
            result_concrete = crate::state::ConcreteValue::from_pyobj(tuple);
        }
        let item_oprefs: Vec<majit_ir::OpRef> = items.iter().map(|i| i.opref).collect();
        let opref = self.trace_build_tuple_value(&item_oprefs, &concrete_items)?;
        Ok(crate::state::FrontendOp::new(opref, result_concrete))
    }

    fn build_map(
        &mut self,
        items: &[Self::Value],
    ) -> Result<Self::Value, pyre_interpreter::PyError> {
        use crate::helpers::TraceHelperAccess;
        let concrete_items: Vec<pyre_object::PyObjectRef> =
            items.iter().map(|i| i.concrete.to_pyobj()).collect();
        let mut result_concrete = crate::state::ConcreteValue::Null;
        if concrete_items.iter().all(|v| !v.is_null()) {
            let dict = pyre_interpreter::build_map_from_refs(&concrete_items)?;
            result_concrete = crate::state::ConcreteValue::from_pyobj(dict);
        }
        let item_oprefs: Vec<majit_ir::OpRef> = items.iter().map(|i| i.opref).collect();
        let opref = self.trace_build_map(&item_oprefs)?;
        Ok(crate::state::FrontendOp::new(opref, result_concrete))
    }

    fn store_subscr(
        &mut self,
        obj: Self::Value,
        key: Self::Value,
        value: Self::Value,
    ) -> Result<(), pyre_interpreter::PyError> {
        // MIFrame parity: trace-only, no concrete mutation during the walk.
        // The emitted IR performs the STORE_SUBSCR in the compiled loop
        // (exactly once), for both root and inline frames; see
        // `store_subscr_value`.
        self.store_subscr_value(
            obj.opref,
            key.opref,
            value.opref,
            obj.concrete.to_pyobj(),
            key.concrete.to_pyobj(),
            value.concrete.to_pyobj(),
        )
    }

    fn list_append(
        &mut self,
        list: Self::Value,
        value: Self::Value,
    ) -> Result<(), pyre_interpreter::PyError> {
        // MIFrame parity: the hook itself is trace-only — the concrete
        // LIST_APPEND mutation is performed by the eval loop
        // (`execute_opcode_step`). The append is recorded as a generic
        // residual call.
        use crate::helpers::TraceHelperAccess;
        self.trace_list_append(list.opref, value.opref)
    }

    fn unpack_sequence(
        &mut self,
        seq: Self::Value,
        count: usize,
    ) -> Result<Vec<Self::Value>, pyre_interpreter::PyError> {
        self.unpack_sequence_value(seq.opref, count, seq.concrete.to_pyobj())
    }

    fn load_attr(
        &mut self,
        obj: Self::Value,
        name: &str,
    ) -> Result<Self::Value, pyre_interpreter::PyError> {
        use crate::helpers::TraceHelperAccess;
        let mut result_concrete = crate::state::ConcreteValue::Null;
        let c_obj = obj.concrete.to_pyobj();
        if !c_obj.is_null() {
            if let Ok(result) = pyre_interpreter::baseobjspace::getattr_str(c_obj, name) {
                result_concrete = crate::state::ConcreteValue::from_pyobj(result);
            }
        }
        // mapdict.py:1479-1537 LOAD_ATTR fast path: fold a plain instance-attr
        // read to guarded inline storage access; falls back to the residual
        // getattr below for every other shape.
        if !c_obj.is_null() {
            if let Some(opref) = self.try_load_attr_fast_path(obj, c_obj, name)? {
                return Ok(crate::state::FrontendOp::new(opref, result_concrete));
            }
        }
        let opref = self.trace_load_attr(obj.opref, name)?;
        Ok(crate::state::FrontendOp::new(opref, result_concrete))
    }

    fn store_attr(
        &mut self,
        obj: Self::Value,
        name: &str,
        value: Self::Value,
    ) -> Result<(), pyre_interpreter::PyError> {
        use crate::helpers::TraceHelperAccess;
        self.trace_store_attr(obj.opref, name, value.opref)
    }
}

/// Generate the trivial `*_constant` methods — those whose body is the
/// uniform `concrete = <ConcreteValue>; opref = <trace>; FrontendOp::new`
/// shape. The structural analog of the `exec(py.code.Source(...).compile())`
/// loops in `pyjitpl.py:279-368` that generate the regular `opimpl_*`
/// families from a name list. Irregular constants (`bigint/bytes/complex/
/// code/slice`) are written out below, like the hand-written `@arguments(...)`
/// opimpls.
macro_rules! trivial_constant_methods {
    ($($name:ident, $trace:ident ( $($arg:ident : $argty:ty),* ) => $concrete:expr;)*) => {
        $(
            fn $name(&mut self $(, $arg: $argty)*)
                -> Result<Self::Value, pyre_interpreter::PyError>
            {
                use crate::helpers::TraceHelperAccess;
                let concrete = $concrete;
                let opref = self.$trace($($arg),*)?;
                Ok(crate::state::FrontendOp::new(opref, concrete))
            }
        )*
    };
}

impl pyre_interpreter::ConstantOpcodeHandler for crate::state::MIFrame {
    trivial_constant_methods! {
        int_constant, trace_int_constant(value: i64)
            => crate::state::ConcreteValue::Int(value);
        float_constant, trace_float_constant(value: f64)
            => crate::state::ConcreteValue::Float(value);
        bool_constant, trace_bool_constant(value: bool)
            => crate::state::ConcreteValue::Int(value as i64);
        str_constant, trace_str_constant(value: &rustpython_wtf8::Wtf8)
            => crate::state::ConcreteValue::Ref(pyre_object::w_str_from_wtf8(value.to_owned()));
        none_constant, trace_none_constant()
            => crate::state::ConcreteValue::Ref(pyre_object::w_none());
        ellipsis_constant, trace_ellipsis_constant()
            => crate::state::ConcreteValue::Ref(pyre_object::special::w_ellipsis());
    }

    fn bigint_constant(
        &mut self,
        value: &pyre_interpreter::PyBigInt,
    ) -> Result<Self::Value, pyre_interpreter::PyError> {
        use crate::helpers::TraceHelperAccess;
        let concrete = crate::state::ConcreteValue::Ref(pyre_object::w_long_new(value.clone()));
        let opref = self.trace_bigint_constant(value)?;
        Ok(crate::state::FrontendOp::new(opref, concrete))
    }

    fn bytes_constant(&mut self, value: &[u8]) -> Result<Self::Value, pyre_interpreter::PyError> {
        use crate::helpers::TraceHelperAccess;
        // pyre lacks a separate bytes type — bytes literals materialise as
        // bytearray. Call sites that need true immutability must copy.
        let bytes_ref = pyre_object::bytearrayobject::w_bytearray_from_bytes(value);
        let concrete = crate::state::ConcreteValue::Ref(bytes_ref);
        let opref = self.trace_bytes_constant(value)?;
        Ok(crate::state::FrontendOp::new(opref, concrete))
    }

    fn complex_constant(
        &mut self,
        re: f64,
        im: f64,
    ) -> Result<Self::Value, pyre_interpreter::PyError> {
        use crate::helpers::TraceHelperAccess;
        let concrete =
            crate::state::ConcreteValue::Ref(pyre_object::complexobject::w_complex_new(re, im));
        let opref = self.trace_complex_constant(re, im)?;
        Ok(crate::state::FrontendOp::new(opref, concrete))
    }

    fn code_constant(
        &mut self,
        code: &pyre_interpreter::CodeObject,
    ) -> Result<Self::Value, pyre_interpreter::PyError> {
        use crate::helpers::TraceHelperAccess;
        let concrete = crate::state::ConcreteValue::Ref(
            code as *const pyre_interpreter::CodeObject as pyre_object::PyObjectRef,
        );
        let opref = self.trace_code_constant(code)?;
        Ok(crate::state::FrontendOp::new(opref, concrete))
    }

    // slice_constant — `pypy/objspace/std/objspace.py:385` `space.newslice`
    // emits a fresh W_SliceObject per call. The IR shape mirrors
    // `pypy/interpreter/pyopcode.py:1463 BUILD_SLICE` →
    // `op.newslice(w_start, w_end, w_step)` so the three operands stay
    // live in the trace as field dependencies (NewWithVtable + 3
    // SetfieldGc per `emit_box_slice_inline`). The concrete shadow mirrors
    // the root interpreter's W_SliceObject so STORE_SUBSCR can take the same
    // list slice-assignment specialization decision PyPy takes after
    // `space.newslice(...)`.
    fn slice_constant(
        &mut self,
        start: Self::Value,
        stop: Self::Value,
        step: Self::Value,
    ) -> Result<Self::Value, pyre_interpreter::PyError> {
        let opref = self.with_ctx(|_this, ctx| {
            let new_op = crate::helpers::emit_box_slice_inline(
                ctx,
                start.opref,
                stop.opref,
                step.opref,
                crate::descr::w_slice_size_descr(),
                crate::descr::slice_w_start_descr(),
                crate::descr::slice_w_stop_descr(),
                crate::descr::slice_w_step_descr(),
            );
            Ok::<_, pyre_interpreter::PyError>(new_op)
        })?;
        let concrete = crate::state::ConcreteValue::Ref(pyre_object::w_slice_new(
            start.concrete.to_pyobj(),
            stop.concrete.to_pyobj(),
            step.concrete.to_pyobj(),
        ));
        Ok(crate::state::FrontendOp::new(opref, concrete))
    }
}

