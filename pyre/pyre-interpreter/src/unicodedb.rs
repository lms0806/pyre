/// Binary-search the disjoint, ascending inclusive-range `table` for `c`.
fn in_ranges(table: &[(u32, u32)], c: char) -> bool {
    let cp = c as u32;
    table
        .binary_search_by(|&(lo, hi)| {
            if hi < cp {
                core::cmp::Ordering::Less
            } else if lo > cp {
                core::cmp::Ordering::Greater
            } else {
                core::cmp::Ordering::Equal
            }
        })
        .is_ok()
}

/// `Numeric_Type=Decimal` (general category `Nd`).
pub fn isdecimal(c: char) -> bool {
    in_ranges(DECIMAL, c)
}

/// `Numeric_Type` in Decimal or Digit.
pub fn isdigit(c: char) -> bool {
    in_ranges(DIGIT, c)
}

/// `Numeric_Type` in Decimal, Digit, or Numeric.
pub fn isnumeric(c: char) -> bool {
    in_ranges(NUMERIC, c)
}

#[cfg(test)]
mod tests {
    use super::{isdecimal, isdigit, isnumeric};

    /// Reference triples `(char, decimal, digit, numeric)` taken from CPython.
    const CASES: &[(char, bool, bool, bool)] = &[
        ('0', true, true, true),
        ('\u{0663}', true, true, true),   // Arabic-Indic three — Nd
        ('\u{1D7F6}', true, true, true),  // Math monospace zero — Nd astral
        ('\u{00B3}', false, true, true),  // Superscript three — Digit, not Decimal
        ('\u{2460}', false, true, true),  // Circled one — Digit, not Decimal
        ('\u{00BD}', false, false, true), // Vulgar fraction half — Numeric only
        ('\u{216B}', false, false, true), // Roman numeral twelve — Nl, Numeric only
        ('\u{4E00}', false, false, true), // CJK 一 — Numeric via Unihan
        ('a', false, false, false),
        (' ', false, false, false),
    ];

    #[test]
    fn matches_cpython_reference() {
        for &(c, dec, dig, num) in CASES {
            assert_eq!(isdecimal(c), dec, "isdecimal({c:?})");
            assert_eq!(isdigit(c), dig, "isdigit({c:?})");
            assert_eq!(isnumeric(c), num, "isnumeric({c:?})");
        }
    }

    #[test]
    fn decimal_subset_digit_subset_numeric() {
        for &(c, ..) in CASES {
            if isdecimal(c) {
                assert!(isdigit(c), "decimal not a subset of digit at {c:?}");
            }
            if isdigit(c) {
                assert!(isnumeric(c), "digit not a subset of numeric at {c:?}");
            }
        }
    }
}
